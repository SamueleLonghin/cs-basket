<h2>3 - FORMULA di SVOLGIMENTO GARE </h2>
<p>Sono ammesse a partecipare le Rappresentative di Istituto vincitrici delle rispettive finali regionali dei Campionati Studenteschi di Pallacanestro 3c3 maschili e femminili a.s. 2018/2019 come certificato dagli Organismi regionali mediante l’invio del “Modello ORSS” allegato.</p>
<p>Per quanto non espressamente citato, si richiamano: il Progetto Tecnico, le Schede Tecniche dei Campionati Studenteschi 2018/2019 e, per quanto non previsto, si rimanda ai Regolamenti della Federazione Italiana Pallacanestro. </p>
<p><strong>Tempi di gioco.</strong><p>In tutte le fasi previste le gare si svolgeranno con un tempo di gioco di 10 minuti a scorrere (il cronometro non verrà mai arrestato), o fino al raggiungimento, da parte di una delle due squadre di 21 punti.</p>

<p><strong>Fase di Qualificazione. </strong>
<p>Le Rappresentative partecipanti alla categoria ALLIEVI saranno suddivise, per sorteggio dal comitato organizzatore, in 4 gironi da 5 squadre, che si incontreranno con la formula “all’italiana”, con gare di sola andata. </p>
<p>Le Rappresentative partecipanti alla categoria ALLIEVE saranno suddivise, per sorteggio dal comitato organizzatore, in 3 gironi da 5 squadre, ed 1 girone da 4 squadre che si incontreranno con la formula “all’italiana”, con gare di sola andata. </p>
<p>Il punteggio in classifica viene attribuito nel seguente modo: Vittoria p.ti 3, pareggio p.ti 1, sconfitta p.ti 0.</p>
<p>Per il calcolo delle classifiche, in caso di parità, si farà riferimento ai criteri riportati all’interno del regolamento tecnico. </p>
<p>Le prime due classificate di ciascun girone accederanno ai “Quarti di finale” che si articoleranno attraverso due gironi di quattro squadre le quali si affronteranno con gare di sola andata. </p>
<p>Anche per i gironi dei “Quarti di Finale” il calcolo della classifica seguirà quanto indicato nella precedente fase di qualificazione.</p>
<p>
Le prime due classificate di ciascun girone dei “Quarti di finale” accederanno alle “Semifinali”. </p>
<p>Le vincenti delle semifinali disputeranno la “Finale” 1° - 2° posto. Non verranno svolte le partite di finale per il terzo e quarto posto.</p>
Le gare di Semifinale e Finale saranno ad eliminazione diretta. In caso di parità al termine del tempo regolare di gioco, si disputerà un tempo supplementare che terminerà con la realizzazione del primo canestro o, eventualmente, a seguito di assegnazione di punto per fallo personale.</p>
Parallelamente alla manifestazione principale le squadre impegnate in un <strong>"Shoot end Skill Contest”</strong>(potrebbe subire variazioni in caso di maltempo).</p>
